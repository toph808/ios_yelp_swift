    func numberOfSectionsInTableView(tableView: UITableView) -> Int {
        return 4
    }
    
    func tableView(tableView: UITableView, titleForHeaderInSection section: Int) -> String? {
        switch(section){
        case 0:
            return ""
        case 1:
            return "Distance"
        case 2:
            return "Sort By:"
        case 3:
            return "Category"
        default:
            return ""
        }
    }
    
    func tableView(tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        switch (section) {
        case 0:
            return 1
        case 1:
            return distances.count
        case 2:
            return sortMethods.count
        case 3:
            return categories.count
        default:
            return 0
        }
    }
    
    func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("SwitchCell", forIndexPath: indexPath) as! SwitchCell
        cell.delegate = self
        
        switch (indexPath.section) {
        case 0:
            cell.switchLabel.text = "Offering a Deal"
            cell.onSwitch.on = isOfferingDealSelected
        case 1:
            cell.switchLabel.text = distances[indexPath.row]["name"]
            cell.onSwitch.on = distanceSwitchStates[indexPath.row] ?? false
        case 2:
            cell.switchLabel.text = sortMethods[indexPath.row]
            cell.onSwitch.on = sortMethodSwitchStates[indexPath.row] ?? false
        case 3:
            cell.switchLabel.text = categories[indexPath.row]["name"]
            cell.onSwitch.on = categorySwitchStates[indexPath.row] ?? false
        default:
            break
        }

        return cell
    }
    
    @IBAction func onCancelButton(sender: AnyObject) {
        dismissViewControllerAnimated(true, completion: nil)
    }
    
    @IBAction func onSearchButton(sender: AnyObject) {
        var filters = [String:AnyObject]()
        var selectedCategories = [String]()
        var selectedDistance = ""
        var selectedSortMethodIndex = 0
        
        for (row, isSelected) in distanceSwitchStates {
            if isSelected {
                selectedDistance = distances[row]["code"]!
            }
        }
        
        for (row, isSelected) in sortMethodSwitchStates {
            if isSelected {
                selectedSortMethodIndex = row
            }
        }
        
        for (row, isSelected) in categorySwitchStates {
            if isSelected {
                selectedCategories.append(categories[row]["code"]!)
            }
        }
        
        filters["is_offering_deal_selected"] = isOfferingDealSelected
        filters["distance"] = selectedDistance
        filters["sort_method_index"] = selectedSortMethodIndex
        filters["categories"] = selectedCategories
        
        delegate?.filtersViewController?(self, didUpdateFilters: filters)
        
        dismissViewControllerAnimated(true, completion: nil)
    }
    
    func switchCell(switchCell: SwitchCell, didChangeValue value: Bool) {
        let indexPath = tableView.indexPathForCell(switchCell)!
        var newIndexPath: NSIndexPath?
        var cell: SwitchCell?
        
        switch (indexPath.section) {
        case 0:
            isOfferingDealSelected = value
        case 1:
            for i in 0...distances.count {
                if i != indexPath.row {
                    newIndexPath = NSIndexPath(forRow: i, inSection: indexPath.section)
                    cell = tableView.cellForRowAtIndexPath(newIndexPath!) as? SwitchCell
                    cell?.onSwitch?.on = false
                }
            }
            distanceSwitchStates[indexPath.row] = value
        case 2:
            for i in 0...sortMethods.count {
                if i != indexPath.row {
                    newIndexPath = NSIndexPath(forRow: i, inSection: indexPath.section)
                    cell = tableView.cellForRowAtIndexPath(newIndexPath!) as? SwitchCell
                    cell?.onSwitch?.on = false
                }
            }
            sortMethodSwitchStates[indexPath.row] = value
        case 3:
            categorySwitchStates[indexPath.row] = value
        default:
            break
        }
    }

